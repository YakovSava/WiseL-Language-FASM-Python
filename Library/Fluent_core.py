"""Ядро Fluent: отрисовка, ховер, клик"""

def paint(wid):
    return [
        # Выбор цвета фона
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .not_click_{wid}",
        f"    invoke CreateSolidBrush, [btn_bg_click_{wid}]",
        f"    jmp .fill_{wid}",
        f"  .not_click_{wid}:",
        f"    cmp [btn_state_{wid}], 1",
        f"    jne .not_hover_{wid}",
        f"    invoke CreateSolidBrush, [btn_bg_hover_{wid}]",
        f"    jmp .fill_{wid}",
        f"  .not_hover_{wid}:",
        f"    invoke CreateSolidBrush, [btn_bg_{wid}]",
        f"  .fill_{wid}:",
        f"    mov r13, rax",
        # Выбор RECT
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .rect_not_click_{wid}",
        f"    lea rdx, [btn_rect_click_{wid}]",
        f"    jmp .do_fill_{wid}",
        f"  .rect_not_click_{wid}:",
        f"    cmp [btn_state_{wid}], 1",
        f"    jne .rect_normal_{wid}",
        f"    lea rdx, [btn_rect_hover_{wid}]",
        f"    jmp .do_fill_{wid}",
        f"  .rect_normal_{wid}:",
        f"    lea rdx, [btn_rect_{wid}]",
        f"  .do_fill_{wid}:",
        f"    invoke FillRect, r12, rdx, r13",
        f"    invoke DeleteObject, r13",
        # Рамка
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .border_not_click_{wid}",
        f"    invoke CreateSolidBrush, [btn_border_click_{wid}]",
        f"    jmp .border_do_{wid}",
        f"  .border_not_click_{wid}:",
        f"    invoke CreateSolidBrush, [btn_border_{wid}]",
        f"  .border_do_{wid}:",
        f"    mov r15, rax",
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .brect_not_click_{wid}",
        f"    mov eax, [btn_rect_click_{wid}.left]",
        f"    mov [border_rect.left], eax",
        f"    mov eax, [btn_rect_click_{wid}.top]",
        f"    mov [border_rect.top], eax",
        f"    mov eax, [btn_rect_click_{wid}.right]",
        f"    mov [border_rect.right], eax",
        f"    mov eax, [btn_rect_click_{wid}.bottom]",
        f"    mov [border_rect.bottom], eax",
        f"    jmp .border_frame_{wid}",
        f"  .brect_not_click_{wid}:",
        f"    mov eax, [btn_rect_{wid}.left]",
        f"    mov [border_rect.left], eax",
        f"    mov eax, [btn_rect_{wid}.top]",
        f"    mov [border_rect.top], eax",
        f"    mov eax, [btn_rect_{wid}.right]",
        f"    mov [border_rect.right], eax",
        f"    mov eax, [btn_rect_{wid}.bottom]",
        f"    mov [border_rect.bottom], eax",
        f"  .border_frame_{wid}:",
        f"    lea rdx, [border_rect]",
        f"    invoke FrameRect, r12, rdx, r15",
        f"    invoke DeleteObject, r15",
        # Цвет шрифта
        f"    cmp [btn_state_{wid}], 2",
        f"    je .fclick_{wid}",
        f"    cmp [btn_state_{wid}], 1",
        f"    je .fhover_{wid}",
        f"    invoke SetTextColor, r12, [btn_font_{wid}]",
        f"    jmp .fdone_{wid}",
        f"  .fclick_{wid}:",
        f"    invoke SetTextColor, r12, [btn_font_click_{wid}]",
        f"    jmp .fdone_{wid}",
        f"  .fhover_{wid}:",
        f"    invoke SetTextColor, r12, [btn_font_hover_{wid}]",
        f"  .fdone_{wid}:",
        f"    invoke SetBkMode, r12, 1",
        # Текст и его позиция
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .text_not_click_{wid}",
        f"    mov eax, [btn_rect_click_{wid}.left]",
        f"    mov [rct.left], eax",
        f"    mov eax, [btn_rect_click_{wid}.top]",
        f"    mov [rct.top], eax",
        f"    mov eax, [btn_rect_click_{wid}.right]",
        f"    mov [rct.right], eax",
        f"    mov eax, [btn_rect_click_{wid}.bottom]",
        f"    mov [rct.bottom], eax",
        f"    lea r8, [_btn_text_click_{wid}]",
        f"    jmp .draw_text_{wid}",
        f"  .text_not_click_{wid}:",
        f"    mov eax, [btn_rect_{wid}.left]",
        f"    mov [rct.left], eax",
        f"    mov eax, [btn_rect_{wid}.top]",
        f"    mov [rct.top], eax",
        f"    mov eax, [btn_rect_{wid}.right]",
        f"    mov [rct.right], eax",
        f"    mov eax, [btn_rect_{wid}.bottom]",
        f"    mov [rct.bottom], eax",
        f"    lea r8, [_btn_text_{wid}]",
        f"  .draw_text_{wid}:",
        f"    lea r9, [rct]",
        f"    invoke DrawText, r12, r8, -1, r9, 37"
    ]

# Library/Fluent_core.py — mousemove с bErase=0
def mousemove(wid):
    return [
        f"    cmp eax, [btn_rect_{wid}.left]",
        f"    jl .out_{wid}",
        f"    cmp eax, [btn_rect_{wid}.right]",
        f"    jg .out_{wid}",
        f"    cmp edx, [btn_rect_{wid}.top]",
        f"    jl .out_{wid}",
        f"    cmp edx, [btn_rect_{wid}.bottom]",
        f"    jg .out_{wid}",
        f"    cmp [btn_state_{wid}], 3",
        f"    je .noredraw_{wid}",
        f"    cmp [btn_state_{wid}], 2",
        f"    jne .not_clicked_{wid}",
        f"    mov [btn_state_{wid}], 3",
        f"    invoke InvalidateRect, rbx, 0, 0",
        f"    jmp .noredraw_{wid}",
        f"  .not_clicked_{wid}:",
        f"    cmp [btn_state_{wid}], 1",
        f"    je .noredraw_{wid}",
        f"    mov [btn_state_{wid}], 1",
        f"    invoke InvalidateRect, rbx, 0, 0",
        f"    jmp .noredraw_{wid}",
        f"  .out_{wid}:",
        f"    cmp [btn_state_{wid}], 3",
        f"    jne .not_leave_click_{wid}",
        f"    mov [btn_state_{wid}], 2",
        f"    invoke InvalidateRect, rbx, 0, 0",
        f"    jmp .noredraw_{wid}",
        f"  .not_leave_click_{wid}:",
        f"    cmp [btn_state_{wid}], 2",
        f"    je .noredraw_{wid}",
        f"    cmp [btn_state_{wid}], 0",
        f"    je .noredraw_{wid}",
        f"    mov [btn_state_{wid}], 0",
        f"    invoke InvalidateRect, rbx, 0, 0",
        f"  .noredraw_{wid}:"
    ]